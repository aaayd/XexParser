    public static string? GetGameName(string titleId)
    {
        var normalized = titleId.ToUpperInvariant()
            .Replace("0X", "")
            .TrimStart('0');

        if (Games.TryGetValue(titleId.ToUpperInvariant(), out var name))
            return name;

        foreach (var kvp in Games)
        {
            var keyNormalized = kvp.Key.ToUpperInvariant().TrimStart('0');
            if (keyNormalized == normalized)
                return kvp.Value;
        }

        return null;
    }

    public static string? GetGameName(uint titleId)
    {
        return GetGameName(titleId.ToString("X8"));
    }

